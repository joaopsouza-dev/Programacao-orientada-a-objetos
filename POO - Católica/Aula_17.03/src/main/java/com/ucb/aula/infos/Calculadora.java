package com.ucb.aula.infos;

public class Calculadora {

    public int somar(int a, int b) {
        int resultado = a + b;
        return resultado;
    }

    public int somar(int a, int b, int c) {
        int resultado = a + b + c;
        return resultado;
    }

    public int somar(int a, int b, int c, int d) {
        int resultado = a + b + c + d;
        return resultado;
    }
}
